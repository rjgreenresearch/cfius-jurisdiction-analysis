def sites_for_regime(sites: List[dict], regime: int) -> List[dict]:
    """Return sites active under a given regime with effective thresholds."""
    if regime == 1:
        return []

    result = []
    for s in sites:
        if not s["latitude"]:
            continue
        added = s["regime_added"]
        part = int(s["current_part"])

        if regime == 2:
            if added in ("2020", "2020_moved_2024"):
                threshold = 1 if added == "2020_moved_2024" else int(s["threshold_miles"])
                result.append({**s, "effective_threshold": threshold})
            elif added == "2020" and part == 0:
                result.append({**s, "effective_threshold": int(s["threshold_miles"])})

        elif regime == 3:
            if added in ("2020", "2020_moved_2024", "2023"):
                threshold = 1 if added == "2020_moved_2024" else int(s["threshold_miles"])
                result.append({**s, "effective_threshold": threshold})
            elif part == 0 and added in ("2020", "2023"):
                result.append({**s, "effective_threshold": int(s["threshold_miles"])})

        elif regime == 4:
            if part > 0:
                result.append({**s, "effective_threshold": int(s["threshold_miles"])})

    return result


def parse_afida_chinese(afida_path: str) -> List[dict]:
    """Parse Chinese-linked holdings from AFIDA 2024 Excel."""
    wb = openpyxl.load_workbook(afida_path, read_only=True)
    ws = wb.active

    holdings = []
    for row in ws.iter_rows(min_row=4, values_only=True):
        cells = [str(c).strip() if c else "" for c in row]
        if len(cells) < 11:
            continue
        country = cells[6]
        if "CHINA" not in country.upper() and "CHINESE" not in country.upper():
            continue
        try:
            acres = float(cells[10])
        except (ValueError, IndexError):
            continue
        if acres <= 0:
            continue

        fips = cells[2].replace(".0", "").strip().zfill(5)
        holdings.append({
            "state": cells[0],
            "county": cells[1],
            "fips": fips,
            "owner": cells[3],
            "country": country,
            "acres": acres,
        })

    wb.close()
    return holdings


def load_centroids(shapefile_path: str) -> Dict[str, Tuple[float, float]]:
    """Load NOAA county centroids from shapefile."""
    sf = shapefile.Reader(shapefile_path)
    fields = [f[0] for f in sf.fields[1:]]
    centroids = {}
    for rec in sf.records():
        fips = str(rec[fields.index("FIPS")]).strip()
        lat = float(rec[fields.index("LAT")])
        lon = float(rec[fields.index("LON")])
        centroids[fips] = (lat, lon)
    return centroids


def run_jurisdiction_analysis(
    holdings: List[dict],
    centroids: Dict[str, Tuple[float, float]],
    sites: List[dict],
) -> List[dict]:
    """Classify each county as covered/not covered under each regime."""

    # Aggregate by county
    county_data = {}
    for h in holdings:
        fips = h["fips"]
        if fips not in centroids:
            continue
        if fips not in county_data:
            lat, lon = centroids[fips]
            county_data[fips] = {
                "fips": fips, "county": h["county"], "state": h["state"],
                "lat": lat, "lon": lon, "entities": [], "total_acres": 0,
            }
        county_data[fips]["entities"].append(h["owner"])
        county_data[fips]["total_acres"] += h["acres"]

    results = []
    for fips, cd in county_data.items():
        row = {
            "fips": fips, "county": cd["county"], "state": cd["state"],
            "lat": cd["lat"], "lon": cd["lon"],
            "entity_count": len(cd["entities"]),
            "total_acres": round(cd["total_acres"], 1),
        }

        for regime in [1, 2, 3, 4]:
            active = sites_for_regime(sites, regime)
            min_dist = float("inf")
            nearest = None
            covered = False
            cov_site = cov_dist = cov_thresh = None

            for s in active:
                d = haversine(cd["lat"], cd["lon"],
                              float(s["latitude"]), float(s["longitude"]))
                if d < min_dist:
                    min_dist = d
                    nearest = s["site_name"]
                if d <= s["effective_threshold"] and not covered:
                    covered = True
                    cov_site = s["site_name"]
                    cov_dist = round(d, 1)
                    cov_thresh = s["effective_threshold"]

            row[f"r{regime}_covered"] = "Y" if covered else "N"
            row[f"r{regime}_nearest_site"] = nearest or "N/A"
            row[f"r{regime}_nearest_dist"] = round(min_dist, 1) if min_dist < float("inf") else "N/A"
            row[f"r{regime}_covering_site"] = cov_site or ""
            row[f"r{regime}_covering_dist"] = cov_dist or ""
            row[f"r{regime}_covering_threshold"] = cov_thresh or ""

            # Part 3: ICBM missile field county FIPS binary test
            # Present from Regime 2 onward (included in original 2020 rule)
            if regime >= 2 and not covered and fips in PART3_FIPS:
                row[f"r{regime}_covered"] = "Y"
                row[f"r{regime}_covering_site"] = "Part 3 ICBM county"
                row[f"r{regime}_covering_dist"] = 0
                row[f"r{regime}_covering_threshold"] = "county"

        results.append(row)

    return results


