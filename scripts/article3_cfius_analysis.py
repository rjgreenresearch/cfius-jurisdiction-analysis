def geocode_appendix_a(appendix_csv: str, mirta_path: str) -> List[dict]:
    """Geocode all Appendix A sites using MIRTA GeoJSON + manual overrides."""

    # Load all MIRTA features
    with open(mirta_path) as f:
        mirta = json.load(f)

    mirta_sites = []
    for feat in mirta["features"]:
        p = feat["properties"]
        geom = feat["geometry"]
        if geom["type"] == "Point":
            lon = geom["coordinates"][0]
            lat = geom["coordinates"][1]
        elif geom["type"] == "Polygon":
            coords = geom["coordinates"][0]
            lat = sum(c[1] for c in coords) / len(coords)
            lon = sum(c[0] for c in coords) / len(coords)
        elif geom["type"] == "MultiPolygon":
            largest = max(geom["coordinates"], key=lambda x: len(x[0]))
            coords = largest[0]
            lat = sum(c[1] for c in coords) / len(coords)
            lon = sum(c[0] for c in coords) / len(coords)
        else:
            continue
        mirta_sites.append({
            "name": (p.get("siteName") or "").strip(),
            "lat": round(lat, 6),
            "lon": round(lon, 6),
        })

    mirta_by_name = {s["name"]: s for s in mirta_sites}

    # Load Appendix A
    rows = []
    with open(appendix_csv) as f:
        for row in csv.DictReader(f):
            rows.append(row)

    results = []
    for a in rows:
        name = a["site_name"]
        lat = lon = ""
        match_name = ""

        # Priority 1: Manual coordinates
        if name in MANUAL_COORDS:
            lat, lon = MANUAL_COORDS[name]
            match_name = "MANUAL_COORDS"

        # Priority 2: MIRTA name override
        elif name in MIRTA_OVERRIDES:
            override = MIRTA_OVERRIDES[name]
            if override in mirta_by_name:
                m = mirta_by_name[override]
                lat, lon = m["lat"], m["lon"]
                match_name = override
            else:
                # Fuzzy match override name
                best_s, best_m = 0, None
                for s in mirta_sites:
                    sc = SequenceMatcher(None, override.lower(), s["name"].lower()).ratio()
                    if sc > best_s:
                        best_s, best_m = sc, s
                if best_m and best_s > 0.5:
                    lat, lon = best_m["lat"], best_m["lon"]
                    match_name = best_m["name"]

        # Priority 3: Auto fuzzy match
        else:
            def norm(s):
                return (s.lower().replace("air force base", "afb")
                        .replace("space force base", "sfb")
                        .replace("space force station", "sfs")
                        .replace("--", " ").replace("-", " ").strip())

            best_s, best_m = 0, None
            for s in mirta_sites:
                sc = SequenceMatcher(None, norm(name), norm(s["name"])).ratio()
                if sc > best_s:
                    best_s, best_m = sc, s
            if best_m and best_s >= 0.60:
                lat, lon = best_m["lat"], best_m["lon"]
                match_name = best_m["name"]

        results.append({
            "site_name": name,
            "location": a["location"],
            "current_part": a["current_part"],
            "threshold_miles": a["threshold_miles"],
            "regime_added": a["regime_added"],
            "latitude": lat,
            "longitude": lon,
            "conus": a["conus"],
            "mirta_match": match_name,
        })

    return results


